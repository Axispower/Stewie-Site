					<div id="leftcolumn_text"> 
						 	<p>The Arkansas Gryphons are located out of Fayetteville, Arkansas. The Gryphons are a part of the Heart of America Rugby Union in the Western Division, and are a Mens 								Division 3 Rugby Club in the United States Rugby hierarchy.</p>
							<p>We field one mens 15s team in the spring and fall, and one mens 7s team in the summer. We practice and play home games at Walker Park in Fayetteville, Arkansas. We  								were founded in the Fall of 2006 and field players from the now defunct Old Arkansas mens team, John Brown University Alumni, the University of Arkansas Alumni, as 								well as players from all over the world. </p>
							<p>If you have played rugby in the past or would like to try and learn to play this competitive sport please consider joining us for training sessions on Tuesdays and  								Thursdays. ALL ARE INVITED. </p>
		
